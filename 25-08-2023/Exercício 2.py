valor = float(input('Digite um valor: '))

if (valor == 1):
    print('O valor é laranja.')

elif (valor == 2):
    print('O valor é verde.')

elif (valor == 3):
    print('O valor é amarelo.')

elif (valor == 4):
    print('O valor é preto.')

else:
    print('Erro! Seu valor não tem cor.')